/* Dichiarazione della classe Hello */

public class Hello
{
  /* Dichiarazione del metodo di lancio main (String
     ha la prima lettera maiuscola perché é un oggetto) */
  
  public static void main (String args[])
  {
    /* Stampo su console */
    System.out.println ("Buongiorno mondo"); /* println va a capo */
    System.out.print ("Eccoci qua... ");     /* print no. */
    System.out.println ("Fine della stampa");
  }
}

/* Per compilare questo file: javac Hello.java */
/* Per eseguire java Hello */
